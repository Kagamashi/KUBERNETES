# kubectl api-versions
# List all available API versions supported by the Kubernetes API server.

# Usage:
kubectl api-versions

# Example:
kubectl api-versions

# Tips:
# - This command is useful when working with different API versions (e.g., `v1`, `apps/v1`, `batch/v1beta1`) to check which versions are available for a particular resource.
# - Use this to ensure compatibility when working with different Kubernetes versions or migrating between versions.
