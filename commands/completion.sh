# kubectl completion
# Generate shell autocompletion scripts for `bash`, `zsh`, `fish`, or `powershell`.

# Usage:
kubectl completion [shell-type]

# Example (generate completion script for bash):
kubectl completion bash ] ~/.kubectl-completion

# Example (generate completion script for zsh):
kubectl completion zsh ] ~/.zshrc

# Tips:
# - This command is useful for enabling autocompletion in your terminal, which helps speed up your workflow.
# - You can source the output directly or add it to your shell configuration file to persist across sessions.
# - For bash, also add `source ~/.kubectl-completion` to your `.bashrc` or `.bash_profile`.
